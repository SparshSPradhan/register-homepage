import React, { useState, useEffect } from 'react';
import { FaStar, FaPlay } from 'react-icons/fa';
import businessImage from '../assets/images/business-image.png';
import heroImage1 from '../assets/images/hero-right-1.png';
import heroImage2 from '../assets/images/hero-right-2.png';  // New image added

const taskCards = [
  { title: "Annual Compliance" },
  { title: "Company Formation" },
  { title: "Payroll Services" },
  { title: "Taxation Services" }
];

const HeroSection = () => {
  const [visibleCards, setVisibleCards] = useState([]);

  useEffect(() => {
    let index = 0;
    const interval = setInterval(() => {
      if (index < taskCards.length) {
        setVisibleCards((prev) => [...prev, taskCards[index]]);
        index++;
      } else {
        clearInterval(interval);
      }
    }, 1000);

    return () => clearInterval(interval);
  }, []);

  return (
    <section className="relative flex flex-col md:flex-row items-center justify-between py-20 px-8 bg-gray-100 overflow-x-hidden">
      {/* Left Side */}
      <div className="md:w-1/2 text-center md:text-left md:ml-16">
        <div className="flex items-center space-x-2 text-orange-500 text-lg">
          <FaStar size={24} /><FaStar size={24} /><FaStar size={24} /><FaStar size={24} /><FaStar size={24} />
          <span className="text-gray-800 font-semibold ml-2 text-xl">Google Rating</span>
        </div>
        <h1 className="text-4xl md:text-5xl font-bold text-gray-900 mt-6 leading-snug">
          Your Trusted Partner <br /> for Compliance Business Needs
        </h1>
        <p className="text-gray-700 mt-6 text-lg leading-relaxed">
          An online business compliance platform that helps entrepreneurs and other individuals with various registrations, tax filings, and other legal matters.
        </p>


        <img src={businessImage} alt="Business" className="mt-10 w-3/4 md:w-2/3" />

        {/* Buttons Section */}
        <div className="mt-8 flex items-center space-x-6">
          <button className="bg-orange-500 text-white py-3 px-6 md:py-4 md:px-8 rounded-full text-lg font-semibold hover:bg-orange-600 transition duration-300">
            Talk An Expert
          </button>

          {/* Vertical Divider */}
          <div className="h-12 border-l-2 border-gray-400 hidden md:block"></div>

          {/* YouTube Play Button */}
          <div className="flex items-center space-x-3 cursor-pointer">
            <div className="bg-red-500 text-white p-4 rounded-full">
              <FaPlay size={20} />
            </div>
            <span className="text-gray-800 font-medium text-lg">See How It Works</span>
          </div>
        </div>

        {/* <img src={businessImage} alt="Business" className="mt-10 w-3/4 md:w-2/3" /> */}
      </div>

      {/* Right Side - Images and Task Cards */}
      <div className="md:w-1/2 flex justify-center md:justify-end relative">
        <div className="relative">
          {/* Main Image */}
          <img 
            src={heroImage1} 
            alt="Hero Right" 
            className="w-full max-w-md md:max-w-lg transform translate-x-[-150px] translate-y-[90px]" 
          />
          {/* New Image aligned to bottom */}
          <img 
            src={heroImage2} 
            alt="Hero Right 2" 
            className="absolute bottom-0 right-[-100px] w-full max-w-xs md:max-w-sm opacity-90 transform translate-x-[-70px] translate-y-[90px]" 
          />
        </div>

        {/* Task Cards - Zig-Zag effect */}
        <div className="absolute top-[10%] right-[-20px] transform -translate-y-1/2 flex flex-col space-y-4">
          {visibleCards.length > 0 && visibleCards.map((card, index) => (
            <div
              key={index}
              className={`p-4 md:p-6 w-40 md:w-52 bg-white shadow-lg rounded-lg text-center font-bold text-gray-900 transition-all duration-700 transform ${
                index % 2 === 0 ? "translate-x-4" : "-translate-x-4"
              }`}
            >
              {card?.title ?? "Loading..."}
            </div>
          ))}
        </div>
      </div>
    </section>
  );
};

export default HeroSection;
